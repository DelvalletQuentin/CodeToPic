package hierarchyView.util;

public class RGB {
	private int red, green, blue;
	
	public RGB(int red, int green, int blue) {
		this.red = red;
		this.green = green;
		this.blue = blue;
	}

	/*
	* Get the red value.
	*/
	public int getRed() {
		return red;
	}

	/*
	* Get the green value.
	*/
	public int getGreen() {
		return green;
	}


	/*
	* Get the blue value.
	*/
	public int getBlue() {
		return blue;
	}
}
